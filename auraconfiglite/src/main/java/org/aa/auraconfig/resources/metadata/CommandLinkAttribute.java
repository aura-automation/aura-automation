package org.aa.auraconfig.resources.metadata;

public class CommandLinkAttribute {

	String linkAttibuteName;
	String targetObjectRelation;
	String targetAttribute;
	String targetObjectAttribute;
	String targetObjectType;
	
	public String getLinkAttibuteName() {
		return linkAttibuteName;
	}
	public void setLinkAttibuteName(String linkAttibuteName) {
		this.linkAttibuteName = linkAttibuteName;
	}
	public String getTargetObjectRelation() {
		return targetObjectRelation;
	}
	public void setTargetObjectRelation(String targetObjectRelation) {
		this.targetObjectRelation = targetObjectRelation;
	}
	public String getTargetAttribute() {
		return targetAttribute;
	}
	public void setTargetAttribute(String targetAttribute) {
		this.targetAttribute = targetAttribute;
	}
	public String getTargetObjectAttribute() {
		return targetObjectAttribute;
	}
	public void setTargetObjectAttribute(String targetObjectAttribute) {
		this.targetObjectAttribute = targetObjectAttribute;
	}
	public String getTargetObjectType() {
		return targetObjectType;
	}
	public void setTargetObjectType(String targetObjectType) {
		this.targetObjectType = targetObjectType;
	}


}
