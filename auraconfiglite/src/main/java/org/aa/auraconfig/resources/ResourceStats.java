package org.aa.auraconfig.resources;

public class ResourceStats {
	
	private int incomingAttributeCnt;
	
	private int newAttributeCnt;
	
	private int modifiedAttributeCnt;

	/**
	 * @return the incomingAttributeCnt
	 */
	public int getIncomingAttributeCnt() {
		return incomingAttributeCnt;
	}

	/**
	 * @param incomingAttributeCnt the incomingAttributeCnt to set
	 */
	public void setIncomingAttributeCnt(int incomingAttributeCnt) {
		this.incomingAttributeCnt = incomingAttributeCnt;
	}

	/**
	 * @return the newAttributeCnt
	 */
	public int getNewAttributeCnt() {
		return newAttributeCnt;
	}

	/**
	 * @param newAttributeCnt the newAttributeCnt to set
	 */
	public void setNewAttributeCnt(int newAttributeCnt) {
		this.newAttributeCnt = newAttributeCnt;
	}

	/**
	 * @return the modifiedAttributeCnt
	 */
	public int getModifiedAttributeCnt() {
		return modifiedAttributeCnt;
	}

	/**
	 * @param modifiedAttributeCnt the modifiedAttributeCnt to set
	 */
	public void setModifiedAttributeCnt(int modifiedAttributeCnt) {
		this.modifiedAttributeCnt = modifiedAttributeCnt;
	}

}
